const insert = db.prepare(
  'INSERT INTO transactions (user, date, type, amount, notes) VALUES (?, ?, ?, ?, ?)'
);

const insertAll = db.transaction(() => {
  let vCount = 0, tCount = 0;
  for (const [vAmt, tAmt, date, notes] of data) {
    if (vAmt != null && vAmt !== 0) {
      const type = vAmt > 0 ? 'deposit' : 'withdrawal';
      insert.run('Valentina', date, type, Math.abs(vAmt), notes);
      vCount++;
    }
    if (tAmt != null && tAmt !== 0) {
      const type = tAmt > 0 ? 'deposit' : 'withdrawal';
      insert.run('Tiana', date, type, Math.abs(tAmt), notes);
      tCount++;
    }
  }
  return { vCount, tCount };
});

// Clear existing transactions first
db.prepare('DELETE FROM transactions').run();

